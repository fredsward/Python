
# Operadores de asignacion compuestos  

print("Operadores de asignacion compuestos")
a ,b = 10, 15
print(f'Valor inicial de a: {a}, b: {b}')

# Opeador compuesto de suma +=
a += b
print(f'Operador a+=b es: {a}')

# Operador compuesto de resta -=
a = 10 # reiniciar valor de a
a -= b
print(f'Operador a-b es: {a}')

# Operador compuesto de multiplicacion *=
a = 10
a *= b
print(f'Operador a*b es: {a}')

# Operador compuesto de division /=
a = 10
a /= b
print(f'Operador a/b es: {a:.2f}')

# Operadores de comparacion ( Relacionales)
a, b = 7 , 5
print(f'Valor inicial de a: {a}, b: {b}')

# Operador de igualdad
resultado = a == b 
print(f'Resultado: {resultado}') #Falso 

# Operador diferente
resultado = a != b
print(f'Resultado: {resultado}') # Verdadero   

# Operador mayor que
resultado = a > b
print(f'Resultado: {resultado}')

